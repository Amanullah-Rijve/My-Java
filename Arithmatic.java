public class Arithmatic{
    public static void main(String[] args){
        int x = 7;
        int y = 9;
        
        System.out.println("+ Operator "+ (x+y));
        System.out.println("- Operator "+( x-y));
        System.out.println("* Operator "+( x*y));
        System.out.println("/ Operator "+ (x/y));
        System.out.println("% Operator "+ (x%y));
    }
}